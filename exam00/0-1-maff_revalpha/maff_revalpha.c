#include <unistd.h>

void	 main()
{
	char a;
	char b;

	a = 'z';
	b = 'Y';
	while (b >= 'A')
	{
		write(1, &a, 1);
		write(1, &b, 1);
		a -= 2;
		b -= 2;
	}
}

