#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	ft_strlen(char *str)
{
	int	i;
	
	i = 0;
	while (str[i])
		i++;
	return (i);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(int ac, char **av)
{
	int	i;
	int	len;

	i = 1;
	len = ac - 1;
	while (len > 0)
	{
		ft_putstr(av[i]);
		ft_putchar('\n');
		i++;
		len--;
	}
	return (0);
}
