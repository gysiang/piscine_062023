#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	len;

	len = ac - 1;
	if (ac != 1)
 	{
		while (len >= 1)
		{
			i = 0;
			while(av[len][i] != '\0')
			{
				write(1, &av[len][i], 1);
				i++;
			}
			write(1, "\n", 1);
			len--;
		}
	}
	return (0);
}
